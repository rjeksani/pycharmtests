# Dictionary type is similar to the Key Value pair like Hashmap or Table with multiple data types in single dictionary
# denoted using flower braces

dict = {1:"laddu" , 2:"pandu", 3:"raghav" , "last":4}

print(dict[1])
print(dict[2])
print(dict[3])
print(dict["last"])

dict[5] = "kris"  # inserting values into a dictionary

print(dict[5])